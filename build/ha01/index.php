      <h1>Von Klarinetten und Buletten</h1>
      <h2>Eine Erzählung in fünf Absätzen</h2>

      <p>
        Sie saß im Wohnzimmer und schrieb einen Brief an Boris Onkel. „Hier sind es −5° C und Boris reicht es jetzt schon mit dem Herbst!“, begann sie. „Er ist den ganzen Tag schon am Jammern… der Schal den du ihm geschenkt hast, hält nicht warm.“
      </p>
      <p>
        Sie fügte hinzu: „Ich habe für Hans Peter übrigens jetzt schon die Lebkuchen gekauft, die er so gerne isst… Bald kann man die Weihnachtssachen direkt im Anschluss an die Sommerferien rausholen (und dann ein halbes Jahr lang nur Marzipan und Dominosteine essen). Bolle ist schon wieder teurer geworden: Diesmal habe ich 3,50 € bezahlt.“<br>
        „Seinen Laptop mit 12″ Bildschirm bringe ich ihm aber wieder mit! Ich schenke ihm das Ding diese Weihnachten einfach wieder zurück.“, unterbrach Boris ihren Schreibfluss, „Der alte Knacker mit seinen ‚Schätzen‘ von eBay hat doch nicht alle Tassen im Schrank… auf dem Teil ist Windows 95 drauf und nicht mal eine halbwegs aktuelle Version von Excel. Geschenk hin oder her – das olle Gerät ist eine Zumutung!“
      </p>
      <p>
        Petra grummelte etwas unverständliches und schrieb weiter. Soll’s ihr doch egal sein, was Boris mit seinem Computerkram macht. „Boris hat jetzt über 100 Miniatur-Rennautos in der Vitrine und ist stolz wie Bolle…“<br>Bolle. Wieso eigentlich Bolle? Während sie sich das fragte, fiel ihr ein, dass sie noch dringend zur Reinigung „Susi’s Wäsche – Paradies“ musste. Danach würde Sie Herrn Prof. Dr. rer. nat. habil. Franz Heitman besuchen, der ihr wöchentlich Klarinettenunterricht gab. Außerdem briet er ganz hervorragende Buletten.
      </p>
      <p>
        „Wir kommen dieses Jahr am 24. Dezember von 15.00–19.00 bei euch vorbei, okay?“ beendete sie ihren Brief, weil sie keine Lust hatte sich weiter schriftlich auszumehren wenn sie sowieso ständig von Boris Zwischenrufen aus der kreativen Phrasenbildung gerissen würde.
      </p>
      <p>
        „Habe ich überhaupt Briefmarken?“, fragte sie sich. Doch, da waren noch welche: 10 × 0,62 € stand auf dem Briefchen.
        Sie unterschrieb, faltete das Papier sorgsam, klebte den Umschlag zu, den sie beim KaDeWe im Sonderangebot für 4,– € gekauft hatte, und verließ beschwingt die Wohnung – wohl wissend, dass es heute Abend noch Klarinetten mit anschließenden Buletten geben würde. „Viel Spaß heute Abend, Boris!“, rief sie, als die Tür schon längst ins Schloss gefallen war.
      </p>

